package model;

/**
 *
 * @author bruno
 */
public class Task {

    private Integer id;
    private Person person;
    private Item item;
    private Category category;

    public Task(Person person, Item item, Category category, Integer id) {
        this.person = person;
        this.item = item;
        this.category = category;
        this.id = id;
    }
    
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Person getPerson() {
        return person;
}

    public void setPerson(Person person) {
        this.person = person;
    }

    public Item getItem() {
        return item;
    }

    public void setItem(Item item) {
        this.item = item;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    
}
